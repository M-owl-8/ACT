const languageDetector = {
  type: 'languageDetector' as const,
  async: true,
  detect: async (callback: (lng: string) => void) => {
    try {
      console.log('ğŸŒ i18n Language Detector - Starting detection...');
      const savedLanguage = await AsyncStorage.getItem('user-language');
      console.log('ğŸŒ i18n Language Detector - Saved language from AsyncStorage:', savedLanguage);
      if (savedLanguage && ['en', 'ru', 'uz', 'es'].includes(savedLanguage)) {
        console.log(`âœ… ğŸŒ i18n Language Detector - Calling callback with saved language: ${savedLanguage}`);
        callback(savedLanguage);
      } else {
        console.log('ğŸŒ i18n Language Detector - No valid saved language, defaulting to English');
        callback('en');
      }
    } catch (error) {
      console.error('ğŸŒ i18n Language Detector Error:', error);
      callback('en');
    }
  },
  init: () => {
    console.log('ğŸŒ i18n Language Detector - init() called');
  },
  cacheUserLanguage: async (lng: string) => {
    try {
      console.log(`ğŸŒ i18n Language Detector - Caching language: ${lng}`);
      await AsyncStorage.setItem('user-language', lng);
      console.log(`âœ… ğŸŒ i18n Language Cached: ${lng}`);
    } catch (error) {
      console.error('ğŸŒ i18n Cache Error:', error);
    }
  },
};

